document.getElementById("search-box").addEventListener("input", function() {
  let input = this.value.toLowerCase();
  let suggestionBox = document.getElementById("suggestions");
  suggestionBox.innerHTML = ""; // Մաքրում ենք նախորդ արդյունքները

  if (input === "") return; // Եթե դատարկ է, ոչինչ չցուցադրել

  let suggestions = heroes.filter(hero => hero.toLowerCase().includes(input));

  suggestions.forEach(hero => {
    let div = document.createElement("div");
    div.innerHTML = highlightMatch(hero, input); // Տեքստի ընդգծում
    div.classList.add("suggestion-item");
    div.onclick = function() {
      document.getElementById("search-box").value = hero;
      suggestionBox.innerHTML = "";
    };
    suggestionBox.appendChild(div);
  });
});

// Ֆունկցիա՝ համապատասխան մասերը կանաչով ընդգծելու համար
function highlightMatch(hero, input) {
  let regex = new RegExp(`(${input})`, "gi");
  return hero.replace(regex, "<span class='highlight'>$1</span>");
}
